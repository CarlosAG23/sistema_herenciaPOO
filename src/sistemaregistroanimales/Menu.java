    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(jrba.isSelected())
        {
            Aves as = new Aves(jtxtNombre.getText(), jcbHabitad.getSelectedItem().toString(), jtxtTipoAlimento.getText(), jcbTipoAlas.getSelectedItem().toString(), jcbTipoPico.getSelectedItem().toString(), Integer.parseInt(jsExtremidades.getValue().toString()));
            VerDatos.setText(VerDatos.getText() + "\n\n" + as.MostrarDatos());
            datos.add(as);
            
            Limpiar();            
        }
        else if(jrbp.isSelected())
        {
            Peces ps = new Peces(jtxtNombre.getText(), jcbHabitad.getSelectedItem().toString(), jtxtTipoAlimento.getText(),Integer.parseInt(jsAletas.getValue().toString()), jcbEsqueleto.getSelectedItem().toString());
            VerDatos.setText(VerDatos.getText() + "\n\n" + ps.MostrarDato());
            datos.add(ps);
            Limpiar();                        
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

