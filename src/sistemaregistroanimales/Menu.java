class Animal
{
    String Nombre;
    String TipoHabitad;
    String TipoAlimento;

    public Animal(String Nombre, String TipoHabitad, String TipoAlimento) {
        this.Nombre = Nombre;
        this.TipoHabitad = TipoHabitad;
        this.TipoAlimento = TipoAlimento;
    }

    public String getNombre() {
        return Nombre;
    }

    public String getTipoHabitad() {
        return TipoHabitad;
    }

    public String getTipoAlimento() {
        return TipoAlimento;
    }
    
}

class Aves  extends Animal
{
    String TipoAlas;
    String TipoPico;
    int NumeroExtremidades;

    public Aves(String Nombre, String TipoHabitad, String TipoAlimento, String TipoAlas, String TipoPico, int NumeroExtremidades) {
        super(Nombre, TipoHabitad, TipoAlimento);
        this.TipoAlas = TipoAlas;
        this.TipoPico = TipoPico;
        this.NumeroExtremidades = NumeroExtremidades;
    }

    public String getTipoAlas() {
        return TipoAlas;
    }

    public String getTipoPico() {
        return TipoPico;
    }

    public int getNumeroExtremidades() {
        return NumeroExtremidades;
    }
    
    public String MostrarDatos(){
        return "Nombre: " + getNombre() + "" + "\n"+
                "Tipo de habitad: " + getTipoHabitad() +""+"\n"+
                "Tipo de alimento: " + getTipoAlimento()+""+"\n"+
                "Tipo de alas: " + getTipoAlas()+""+"\n"+
                "Tipo de pico: " + getTipoPico()+""+"\n"+
                "Numero de extremidades: " + getNumeroExtremidades()+""+"\n";
    }
    
}

class Peces extends Animal
{
    int NumeroAletas;
    String TipoEsqueleto;

    public Peces(String Nombre, String TipoHabitad, String TipoAlimento, int NumeroAletas, String TipoEsqueleto) {
        super(Nombre, TipoHabitad, TipoAlimento);
        this.NumeroAletas = NumeroAletas;
        this.TipoEsqueleto = TipoEsqueleto;
    }

    public int getNumeroAletas() {
        return NumeroAletas;
    }

    public String getTipoEsqueleto() {
        return TipoEsqueleto;
    }
    
    public String MostrarDato(){
        return "Nombre: " + getNombre() + "" + "\n"+
                "Tipo de habitad: " + getTipoHabitad() +""+"\n"+
                "Tipo de alimento: " + getTipoAlimento()+""+"\n"+
                "Numero de aletas: " + getNumeroAletas()+""+"\n"+
                "Tipo de esqueleto: " + getTipoEsqueleto()+""+"\n";
    }    
}
